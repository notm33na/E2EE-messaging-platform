import { defineConfig } from 'vite';
import react from '@vitejs/plugin-react';
import mkcert from 'vite-plugin-mkcert';
import os from 'os';

// Get local network IP address
function getLocalIP() {
  const interfaces = os.networkInterfaces();
  for (const name of Object.keys(interfaces)) {
    for (const iface of interfaces[name]) {
      if (iface.family === 'IPv4' && !iface.internal) {
        return iface.address;
      }
    }
  }
  return null;
}

const localIP = getLocalIP();
const hostnames = ['localhost', '127.0.0.1'];
if (localIP) {
  hostnames.push(localIP);
}

// https://vitejs.dev/config/
export default defineConfig({
  plugins: [
    react(),
    mkcert({
      hostnames: hostnames // Include localhost, 127.0.0.1, and your local IP
    })
  ],
  server: {
    host: true, // Expose server on network
    port: 5173,
    https: true, // Enable HTTPS (certificate generated by mkcert plugin)
    hmr: {
      port: 5173,
      clientPort: 5173,
      protocol: 'wss', // Use secure WebSocket for HMR over HTTPS
    },
    proxy: {
      '/api': {
        target: 'https://localhost:8443',
        changeOrigin: true,
        secure: false, // Allow self-signed certificates
        rewrite: (path) => path,
        configure: (proxy, options) => {
          proxy.on('error', (err, req, res) => {
            console.warn('Proxy error:', err.message);
            console.warn('Make sure the backend server is running on https://localhost:8443');
          });
        }
      },
      '/socket.io': {
        target: 'https://localhost:8443',
        changeOrigin: true,
        secure: false, // Allow self-signed certificates
        ws: true, // Enable WebSocket proxying
        rewrite: (path) => path
      }
    }
  }
});

